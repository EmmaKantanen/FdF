#include "fdf.h"

int	color_pick(t_dot a, t_dot b)
{
	if (a.z <= 0 || b.z <= 0)
		return (0x69FF62);
	if ((a.z > 0 && a.z < 20) || (b.z > 0 && b.z < 20))
		return (0x8A5353);
	if (a.z >= 20 || b.z >= 20)
		return (0xFFFFFF);
	return (0);
}
